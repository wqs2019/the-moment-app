const fs = require('fs');
const path = require('path');

const sample = (arr) => arr[Math.floor(Math.random() * arr.length)];
const randomInt = (min, max) => Math.floor(Math.random() * (max - min + 1)) + min;

const tasks = [];

// 辅助函数：生成指定数量的任务
function generateTasks(audience, templates, count) {
  for (let i = 0; i < count; i++) {
    const tpl = sample(templates);
    tasks.push({
      title: tpl.title(),
      description: tpl.desc(),
      durationMinutes: tpl.duration ? tpl.duration() : randomInt(1, 4),
      energyValue: tpl.energy ? tpl.energy() : randomInt(10, 25),
      targetAudience: [audience, ...(tpl.extraAudiences || [])],
      scenes: tpl.scenes,
      category: tpl.category,
      vibes: tpl.vibes,
    });
  }
}

